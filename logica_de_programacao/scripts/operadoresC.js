/*
Operadores de comparação 
> maior que
>= maior que ou igual a
< menor que
<= menor ou iguak a
== igualdade (valor)
=== igualdade estrita (valor e tipo)
!= diferente (valor)
!== diferente estrito (valor e tipo)
*/

//compara valores, retorno False
console.log(5>10)
//compara dois valores se e maior ou igual, return True
console.log(5>=5)
//igualando valor, retorn True
console.log("texto"=="texto")
//igualdade restrita valore e tipo, retorno False
console.log("5"===5)
//diferente, retorno True
console.log("texto"!="paragrafo")
//diferença restrita, retorno True
console.log("5"!==5)